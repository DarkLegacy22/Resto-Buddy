    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int flag=0;
        String mana_Rid = jTextField2.getText().toString();
        String mana_Name = jTextField1.getText().toString();
        String mana_Pass = jPasswordField1.getText().toString();
        String mana_Cont = jTextField3.getText().toString();
        String mana_Sala = jTextField4.getText().toString();
        
        if(mana_Rid.equals(""))
            JOptionPane.showMessageDialog(null,"Restaurant Name cannot be Empty.");
        if(mana_Name.equals(""))
            JOptionPane.showMessageDialog(null,"Restaurant Name cannot be Empty.");
        if(mana_Pass.equals(""))
            JOptionPane.showMessageDialog(null,"Password cannot be Empty.");
        if(mana_Cont.equals(""))
            JOptionPane.showMessageDialog(null,"Conatct Details cannot be Empty.");
        if(mana_Sala.equals(""))
            JOptionPane.showMessageDialog(null,"Restaurant Location cannot be Empty.");

        
        
        String check = "[0-9]+";
        if(!mana_Cont.matches(check))
            JOptionPane.showMessageDialog(null,"Enter a valid Contact Number.");
        if(!mana_Sala.matches(check))
            JOptionPane.showMessageDialog(null,"Enter a valid Salary Amount.");
        else{
            JOptionPane.showMessageDialog(null,"Valid entries");
            flag=1;
        }
        
        if(flag==1){
            String rid = jTextField2.getText();
            
            rid=rid.substring(4);
            
            String name = jTextField1.getText();
            String password   = String.valueOf(jPasswordField1.getPassword());
            String contact = jTextField3.getText();
            String salary = jTextField4.getText();

            try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/dbmsmini","root","mysql");
                String query = "{call resman(?,?,?,?,?)}";
                CallableStatement stmt = con.prepareCall(query);
                stmt.setString(1, rid);
                stmt.setString(2, name);
                stmt.setString(3, password);
                stmt.setString(4, contact);
                stmt.setString(5, salary);
                ResultSet rs = stmt.executeQuery();
                String sql = "SELECT manager_id from manager order by manager_id desc limit 1";
                rs = stmt.executeQuery(sql);
                rs.next();
                int i=rs.getInt(1);
                JOptionPane.showMessageDialog(null,"Man" + i + " Is The New Id for Manager!! "
                        + "Remeber this for further use!!");
                con.close();
            }catch(SQLException ex){
                Logger.getLogger(ManagerReg.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex){
                Logger.getLogger(ManagerReg.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.setVisible(false);
            new OwnerPage1().setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

